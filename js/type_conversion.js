// Konversi string ke number
let angkastr = "12";
let angkanum = Number(angkastr);
console.log(angkanum);
console.log(typeof angkanum);

// konversi number ke string
let num = 13;
let numstr = String(num);
console.log(numstr);
console.log(typeof numstr);

// konversi boolean ke number
let salah = false;
let numSalah = Number(salah);
console.log(numSalah);
console.log(typeof numSalah);