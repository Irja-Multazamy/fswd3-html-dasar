// Basic function
function panggil(nama) {
    console.log("Hallo " + nama + "!");
  }
  
  panggil("Ryan");
  
// Function Expression
const panggil2 = function(name) {
    console.log("Hallo " + name + "!");
  }
  
  panggil2("Dita");
  
// Arrow function
const panggil3 = (hai) => {
    console.log("Hallo " + hai + "!");
  }
  
  panggil3("Donat");
