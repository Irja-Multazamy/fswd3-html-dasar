// Aritmatic Operator
let a = 4;
let b = 3;
console.log("Penjumlahan 4 + 3 = ", a + b);
console.log("Pengurangan 4 - 3 = ", a - b);

// Assignment Operator
let c = 2;
c += 3;
console.log(c);

// Comparison Operator
let d = 5;
let e = 8;
console.log(d < e);
console.log(d > e);
console.log(d == e);

// Logical Operator
let f = true;
let g = false;
console.log("Logika AND : ", f && g);
console.log("Logika OR : ", f || g);
console.log("Logika Negasi/Kebalikan : ", !g);

// Bitwise Operator
let h = 00100101;
let i = 01101110;
console.log("Bitwise AND : ", h & i);
console.log("Bitwise OR : ", h | i);

// Ternary Operator
let j = 1;
let k = 1;
console.log(j == k ? "yes" : "no" );